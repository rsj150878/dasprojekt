package com.app.veranstaltung;

public class VeranstaltungsRichter {
	
	private Integer idRichter;
	private Integer idVeranstaltung;
	private Integer lfdNummer;
	private String richterName;
	public Integer getIdRichter() {
		return idRichter;
	}
	public void setIdRichter(Integer idRichter) {
		this.idRichter = idRichter;
	}
	public Integer getIdVeranstaltung() {
		return idVeranstaltung;
	}
	public void setIdVeranstaltung(Integer idVeranstaltung) {
		this.idVeranstaltung = idVeranstaltung;
	}
	public Integer getLfdNummer() {
		return lfdNummer;
	}
	public void setLfdNummer(Integer lfdNummer) {
		this.lfdNummer = lfdNummer;
	}
	public String getRichterName() {
		return richterName;
	}
	public void setRichterName(String richterName) {
		this.richterName = richterName;
	}
	

}
