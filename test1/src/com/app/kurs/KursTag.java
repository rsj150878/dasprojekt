package com.app.kurs;

public class KursTag {

	private Integer idKursTag;
	private Integer version;
	private String bezeichnung;
	private Integer idKurs;

	public Integer getIdKursTag() {
		return idKursTag;
	}

	public void setIdKursTag(Integer idKursTag) {
		this.idKursTag = idKursTag;
	}

	public Integer getVersion() {
		return version;
	}

	public void setVersion(Integer version) {
		this.version = version;
	}

	public String getBezeichnung() {
		return bezeichnung;
	}

	public void setBezeichnung(String bezeichnung) {
		this.bezeichnung = bezeichnung;
	}

	public Integer getIdKurs() {
		return idKurs;
	}

	public void setIdKurs(Integer idKurs) {
		this.idKurs = idKurs;
	}

}
